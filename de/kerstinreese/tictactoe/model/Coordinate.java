package de.kerstinreese.tictactoe.model;

public class Coordinate {

	private int row;
	private int column;

	public Coordinate() {

	}

	public void setRow(int row) {
		this.row = row;
	}

	public void setColumn(int column) {
		this.column = column;
	}

	public int getRow() {
		return row;
	}

	public int getColumn() {
		return column;
	}

}
